<template>
    <div>
        <div v-if="!actualFeature || (actualFeature.length > 0 && allowMoreThanOne !== undefined)">
            <button @click="$store.commit('setOpenResearch', true)" class="border border-blue-500 py-1 px-2 text-sm hover:underline rounded text-blue-500">Add {{ feature }}</button>
        </div>
        <div v-if="$store.getters.openResearch" class="fixed z-10 inset-0 overflow-y-auto" aria-labelledby="modal-title" role="dialog" aria-modal="true">
            <div class="flex items-end justify-center min-h-screen pt-4 px-4 pb-20 text-center sm:block sm:p-0">
                <div class="fixed inset-0 bg-gray-500 opacity-75 transition-opacity" aria-hidden="true"></div>

                <!-- This element is to trick the browser into centering the modal contents. -->
                <span class="hidden sm:inline-block sm:align-middle sm:h-screen" aria-hidden="true">&#8203;</span>
            
                <div class="inline-block align-bottom bg-white rounded-lg px-4 pt-5 pb-4 text-left overflow-hidden shadow-xl transform transition-all sm:my-8 sm:align-middle sm:max-w-lg sm:w-full sm:p-6">
                    <div>
                        <research v-model="form" />
                    </div>
                    <div v-for="(item, key) in $store.getters.featureErrors" :key="key">
                        <div v-for="error in item" :key="error" class="text-red-500">{{ error  }}</div>
                    </div>
                    <div class="mt-5 sm:mt-6 sm:grid sm:grid-cols-2 sm:gap-3 sm:grid-flow-row-dense">
                        <button 
                            @click="createFeature"
                            type="button"
                            class="w-full inline-flex justify-center rounded-md border border-transparent shadow-sm px-4 py-2 bg-indigo-600 text-base font-medium text-white hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500 sm:col-start-2 sm:text-sm"
                        >
                            Create {{ feature }}
                        </button>
                        <button 
                            @click=" $store.commit('setOpenResearch', false)"
                            type="button" 
                            class="mt-3 w-full inline-flex justify-center rounded-md border border-gray-300 shadow-sm px-4 py-2 bg-white text-base font-medium text-gray-700 hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500 sm:mt-0 sm:col-start-1 sm:text-sm"
                        >
                            Cancel
                        </button>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
import { ref } from 'vue';
import Research from './Features/Research';

export default {
    props: ['feature', 'allowMoreThanOne'],
    components: { Research },
    computed: {
        actualFeature() {
            return this.$store.getters.features[this.feature]
        },
    },
    methods: {
        createFeature() {
            this.$store.dispatch('createFeature', {
                ...this.form,
                feature: this.feature,
            })
            this.open = false;
        },
    },

    data() {
        return {
            form: {
                name: '',
                settings: {

                }
            },
        }
    },

    setup() {
        return {
            open: ref(false),
        }
    }
}
</script>